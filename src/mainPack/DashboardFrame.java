    private void buttonCustomerDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCustomerDeleteActionPerformed

        int id = selectedCustomer.getId();

        String[] options = new String[2];
        options[0] = "Evet";
        options[1] = "Hayır";
        int dialogResult = JOptionPane.showOptionDialog(rootPane, "Seçili müşteriyi silmek istediğinize emin misiniz? ", "Müşteri Sil", 0, JOptionPane.INFORMATION_MESSAGE, null, options, null);

        if (dialogResult == JOptionPane.YES_OPTION) {

            DB db = new DB();
            int status = db.deleteCustomer(id);
            db.close();
            if (status > 0) {
                refreshCustomerTable();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Silme işlemi başarısız!");

            }
        }

        buttonCustomerDelete.setEnabled(false);
        buttonCustomerUpdate.setEnabled(false);
        buttonOrderAddDialog.setEnabled(false);


    }//GEN-LAST:event_buttonCustomerDeleteActionPerformed

