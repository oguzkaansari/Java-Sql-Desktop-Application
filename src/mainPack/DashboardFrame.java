    private void buttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSearchActionPerformed

        String name = txtSearchName.getText().trim();
        String surName = txtSearchSurName.getText().trim();

        tableCustomers.setRowSorter(sorterCustomerTable);

        if (name.length() != 0 && surName.length() != 0) {
            sorterCustomerTable.setRowFilter(RowFilter.regexFilter(name, 1));
            sorterCustomerTable.setRowFilter(RowFilter.regexFilter(surName, 2));
        } else if (name.length() != 0 && surName.length() == 0) {
            sorterCustomerTable.setRowFilter(RowFilter.regexFilter(name, 1));
        } else if (name.length() == 0 && surName.length() != 0) {
            sorterCustomerTable.setRowFilter(RowFilter.regexFilter(surName, 2));
        } else {
            sorterCustomerTable.setRowFilter(null);
        }

    }//GEN-LAST:event_buttonSearchActionPerformed

